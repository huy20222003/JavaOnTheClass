
public class bai_2 {
	public static void main(String[] args) {
		float a = -10f, b = 20f;
		
		System.out.println("Gia tri nho nhat la: " + Math.min(a, b));
		System.out.println("Gia tri lon nhat la: " + Math.max(a, b));
		System.out.println("Gia tri tuyet doi cua a la: " + Math.abs(a));
		System.out.println("Gia tri mu 3 cua b la: " + Math.pow(b, 3));
		System.out.println("Gia tri can bac 2 cua b la: " + Math.sqrt(b));
	}
}
